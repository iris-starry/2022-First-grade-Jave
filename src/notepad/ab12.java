package notepad;

public class ab12{
	public static void main(String args[ ]){
		int a =10;
		int b = ++a +10;
		System.out.println(a);
		System.out.println(b);
		a =10;
		b = a++ + 10;
		System.out.println(a);
		System.out.println(b);
		a=10;
		b=--a+10;
		System.out.println(a);
		System.out.println(b);
		a=10;
		b=a--+10;
		System.out.println(a);
		System.out.println(b);
		
		
	}
}
