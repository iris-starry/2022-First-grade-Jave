package notepad;

public class If_Example{
	public static void main(String args[]){
		int num =2;
		if(num == 1){
			System.out.println("1입니다.");
		}
		else if (num == 2){
			System.out.println("2입니다.");
		}
		else if (num == 3){
			System.out.println("3입니다.");
		}
		else {
			System.out.println("그 외의 숫자입니다.");
		}
	}
}
